"use strict";
// Functions and function parameters
function sum(n1, n2) {
    let sum = n1 + n2;
    return sum;
}
console.log(sum(2, 3));
// Arrays
let names = ["Akbar Ali", "Kamran Hanif", "Mani", "Bilal"];
console.log(names);
console.log(names[0]);
// Push Method in Array
names.push("Faizan");
console.log(names);
// Pop Method in Array
names.pop();
console.log(names);
// Shift Method in Array
names.shift();
console.log(names);
// UnShift Method in Array
names.unshift("Akbar Ali");
console.log(names);
